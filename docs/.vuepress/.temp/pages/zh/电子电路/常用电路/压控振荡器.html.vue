<p>压控振荡器（VCO，Voltage Controlled Oscillator），是指一个振荡电路，它的输出频率受外部电压控制，当外部电压在一定范围内改变时，该电路的输出频率相应改变。有两种压控振荡器：输出为正弦波的，以及输出为方波的。本节讲述后者。</p>
